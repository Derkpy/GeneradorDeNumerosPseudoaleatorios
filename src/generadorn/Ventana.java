    private void btnrActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnrActionPerformed
        DefaultListModel n2 = new DefaultListModel();
        FileWriter texto;
        
        int xn = Integer.parseInt(txtsemilla.getText());       
        int a = Integer.parseInt(txta.getText());
        int c = Integer.parseInt(txtc.getText());
        int m = Integer.parseInt(txtm.getText());
        int total = Integer.parseInt(txtotal.getText());
        int numero;
        float numero2;
        
        try {
            texto = new FileWriter("C:\\Gabi\\Public\\PRACTICA SIMULACION\\leeme.txt");
        
        for(int i = 0; i < total; i++){
            numero = (a*xn+c) % m;
            numero2 = (float)numero /(float)(m);
            n2.addElement("r" +(i+1) + ":  "+numero2);
            texto.write(numero2 +"\n");
            xn = numero;
            
        }
        jList1.setModel(n2);
        texto.close();
        }catch (IOException e){
            JOptionPane.showMessageDialog(null, "El archivo no existe");
        }
        
    }//GEN-LAST:event_btnrActionPerformed

